import faker from 'faker'

describe('Signup', () => {
  beforeEach(() => {
    cy.visit('signup')
  })
  it('Should load with correct initial state', () => {
    cy.getByTestId('name-status')
      .should('have.attr', 'title', 'Campo obrigat贸rio')
      .should('contain.text', '')
    cy.getByTestId('email-status')
      .should('have.attr', 'title', 'Campo obrigat贸rio')
      .should('contain.text', '')
    cy.getByTestId('password-status')
      .should('have.attr', 'title', 'Campo obrigat贸rio')
      .should('contain.text', '')
    cy.getByTestId('passwordConfirmation-status')
      .should('have.attr', 'title', 'Campo obrigat贸rio')
      .should('contain.text', '')
    cy.get('button[type=submit]').should('have.attr', 'disabled')
    cy.getByTestId('error-wrap').should('not.have.descendants')
  })

  it('Should present error state if form is invalid', () => {
    cy.getByTestId('name').type(faker.random.alphaNumeric(2))
    cy.getByTestId('name-status')
      .should('have.attr', 'title', 'Campo inv谩lido')
      .should('contain.text', '')
    cy.getByTestId('email').type(faker.random.word())
    cy.getByTestId('email-status')
      .should('have.attr', 'title', 'Campo inv谩lido')
      .should('contain.text', '')
    cy.getByTestId('password').type(faker.random.alphaNumeric(2))
    cy.getByTestId('password-status')
      .should('have.attr', 'title', 'Campo inv谩lido')
      .should('contain.text', '')
    cy.getByTestId('passwordConfirmation').type(faker.random.alphaNumeric(2))
    cy.getByTestId('passwordConfirmation-status')
      .should('have.attr', 'title', 'Campo inv谩lido')
      .should('contain.text', '')
    cy.get('button[type=submit]').should('have.attr', 'disabled')
    cy.getByTestId('error-wrap').should('not.have.descendants')
  })
})
